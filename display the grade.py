p=int(input("enter a percentage"))
if p<=30:
    print("D")
elif p>=31 and p<=40:
    print("C")
elif p>=41 and p<=50:
    print("C+")
elif p>=51 and p<=60:
    print("B")
elif p>=61 and p<=70:
    print("B+")
elif p>=71 and p<=80:
    print("A")
elif p>=80 and p<=100:
    print("A+")
else:
    print("failed")
   

